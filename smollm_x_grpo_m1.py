def extract_xml_answer_eval(text: str) -> str:
    """Extract from <answer>...</answer> tags (matches training format)."""
    if "<answer>" in text and "</answer>" in text:
        ans = text.split("<answer>")[-1].split("</answer>")[0].strip()
        return ans
    return ""

def extract_number(text: str):
    """Fallback: pull the last number from a string."""
    nums = re.findall(r"[+-]?\d+(?:,\d{3})*(?:\.\d+)?", text)
    if nums:
        return nums[-1].replace(",", "")
    return None

def normalize_numeric(val: str) -> str:
    """Normalize to a comparable numeric string."""
    try:
        return str(int(float(val.replace(",", ""))))
    except (ValueError, OverflowError):
        return val.strip()

