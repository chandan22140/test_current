    batch_questions = [test_data[i]["question"] for i in range(start_idx, end_idx)]
    batch_gt_raw = [test_data[i]["answer"] for i in range(start_idx, end_idx)]
    batch_gt = [extract_hash_answer(a) for a in batch_gt_raw]

    # Tokenize all prompts
    all_input_ids = []
    for question in batch_questions:
        messages = [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": question},
        ]
        ids = tokenizer.apply_chat_template(
            messages, tokenize=True, add_generation_prompt=True
        )
        all_input_ids.append(ids)

    # Left-pad to the longest prompt
    max_len = max(len(ids) for ids in all_input_ids)
    padded_ids = []
    attention_masks = []
    for ids in all_input_ids:
        pad_len = max_len - len(ids)
        padded_ids.append([tokenizer.pad_token_id] * pad_len + ids)
        attention_masks.append([0] * pad_len + [1] * len(ids))

    input_ids = torch.tensor(padded_ids, dtype=torch.long, device=device)
    attention_mask = torch.tensor(attention_masks, dtype=torch.long, device=device)

    with torch.no_grad():
        gen_ids = model.generate(
            input_ids,
            attention_mask=attention_mask,
            max_new_tokens=786,
            do_sample=False,
            temperature=None,
            top_p=None,
        )

    # Decode and evaluate each sample
    for j in range(len(batch_questions)):
        gen_start = max_len
        gen_text = tokenizer.decode(gen_ids[j][gen_start:], skip_special_tokens=True)

        pred_ans = extract_xml_answer_eval(gen_text)
        if pred_ans:
            method = "xml"
        else:
            pred_ans = extract_number(gen_text) or ""
            method = "number_fallback" if pred_ans else "none"
        method_stats[method] += 1

        pred_norm = normalize_numeric(pred_ans) if pred_ans else "NONE"
        gt_norm = normalize_numeric(batch_gt[j]) if batch_gt[j] else "NONE"
        is_correct = pred_norm == gt_norm
        correct += int(is_correct)
        total += 1

        status = "✓" if is_correct else "✗"
        if total <= 5 or total % 50 == 0 or not is_correct:
            print(f"  [{total}] {status}  GT={gt_norm}  Pred={pred_norm} ({method})")

    acc = correct / total * 100
    print(f"  ... {total}/{len(test_data)} done — accuracy: {acc:.2f}%")

